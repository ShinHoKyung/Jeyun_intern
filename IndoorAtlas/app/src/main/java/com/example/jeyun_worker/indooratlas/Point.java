package com.example.jeyun_worker.indooratlas;

public class Point {

    public String name;
    public double Latitude;
    public double Longitude;
    public int floor;

    public Point(String name, double Latitude, double Longitude, int floor)
    {
        this.name = name;
        this.Latitude = Latitude;
        this.Longitude = Longitude;
        this.floor = floor;
    }
}
